// models/ExtraFeature.js
const mongoose = require("mongoose");

const ExtraFeatureSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
      min: 0,
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("ExtraFeature", ExtraFeatureSchema);
